TRANSLATIONS = {
    'es': ES,
    'de': DE,
    'it': IT,
    'pt': PT,
    'nl': NL,
}


def flatten_keys(obj, prefix=''):
    """Walk a dict/list tree and return a flat set of dotted `[i]`-indexed keys.

    Scalars become keys. Dicts/lists recurse with prefixed keys, e.g.
    `soloFeatures[0]`, `soloFeatures[1]`, ...
    """
    keys = set()
    if isinstance(obj, dict):
        for k, v in obj.items():
            full = f'{prefix}.{k}' if prefix else k
            keys |= flatten_keys(v, full)
    elif isinstance(obj, list):
        for i, v in enumerate(obj):
            full = f'{prefix}[{i}]'
            keys |= flatten_keys(v, full)
    else:
        keys.add(prefix)
    return keys


def main():
    # Sanity-check: every locale dict must have the same flat key set
    base_keys = flatten_keys(ES)
    for loc, d in TRANSLATIONS.items():
        if flatten_keys(d) != base_keys:
            missing = base_keys - flatten_keys(d)
            extra = flatten_keys(d) - base_keys
            raise SystemExit(f'[{loc}] key mismatch — missing: {missing}, extra: {extra}')

    # Verify each locale dict mirrors the FR structure (same key set, same
    # list lengths). We compare against the FR `pro` namespace structure.
    with (ROOT / 'fr.json').open() as f:
        fr_data = json.load(f)
    fr_pro = fr_data['pro']

    fr_flat = flatten_keys(fr_pro)
    if fr_flat != base_keys:
        missing_in_tr = fr_flat - base_keys
        extra_in_tr = base_keys - fr_flat
        raise SystemExit(
            f'Structure mismatch with FR!\n'
            f'  Missing in translations: {missing_in_tr}\n'
            f'  Extra in translations:   {extra_in_tr}'
        )

    # Write each translation
    for loc, d in TRANSLATIONS.items():
        path = ROOT / f'{loc}.json'
        with path.open() as f:
            data = json.load(f)
        data['pro'] = d
        with path.open('w') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
            f.write('\n')
        print(f'{loc}: pro namespace updated ({len(d)} keys)')

    print('\nDone. Verifying JSON validity…')
    for loc in TRANSLATIONS:
        path = ROOT / f'{loc}.json'
        with path.open() as f:
            json.load(f)
        print(f'  {loc}.json — valid')


if __name__ == '__main__':
    main()
