def update_locale(path: Path, updates: dict):
    """Update a locale JSON file with the given top-level updates.

    `updates` maps top-level key -> dict (deep merged). Existing keys are
    overwritten only by the corresponding sub-key, not the whole namespace.
    """
    data = json.loads(path.read_text(encoding='utf-8'))
    for top_key, sub_dict in updates.items():
        if top_key in data and isinstance(data[top_key], dict) and isinstance(sub_dict, dict):
            data[top_key].update(sub_dict)
        else:
            data[top_key] = sub_dict
    path.write_text(json.dumps(data, ensure_ascii=False, indent=2) + '\n', encoding='utf-8')
    print(f"Updated {path.name}: added {sum(len(v) for v in updates.values())} keys across {len(updates)} namespaces")


