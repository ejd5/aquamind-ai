def main() -> int:
    for locale in LOCALES:
        path = LOCALES_DIR / f'{locale}.json'
        if not path.exists():
            print(f'❌ Missing file: {path}')
            return 1
        with path.open('r', encoding='utf-8') as f:
            data = json.load(f)
        # Overwrite our namespaces (we own them).
        data['academy'] = ACAD[locale]
        data['seo'] = SEO[locale]
        with path.open('w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
            f.write('\n')
        print(f'✅ {locale}: academy ({len(ACAD[locale])} keys) + seo ({len(SEO[locale])} keys)')

    # Consistency check: same key set across locales.
    ref_keys_acad = set(ACAD['fr'].keys())
    ref_keys_seo = set(SEO['fr'].keys())
    for locale in LOCALES:
        if set(ACAD[locale].keys()) != ref_keys_acad:
            print(f'❌ {locale} academy keys mismatch')
            return 1
        if set(SEO[locale].keys()) != ref_keys_seo:
            print(f'❌ {locale} seo keys mismatch')
            return 1
    print(f'✅ Consistency OK — {len(ref_keys_acad)} academy + {len(ref_keys_seo)} seo keys × 7 locales')
    return 0


if __name__ == '__main__':
    raise SystemExit(main())
