# Now: write each namespace to each locale file.
# For FR + EN: use the values defined above.
# For es/de/it/pt/nl: write the same EN values (the gap-fill script would
# do this anyway, but doing it explicitly here keeps the locale files
# consistent and avoids an extra round-trip).
# ────────────────────────────────────────────────────────────────────────────
ALL_NAMESPACES = {
    'proFeatures': PRO_FEATURES,
    'proSolo': PRO_SOLO,
    'proTeam': PRO_TEAM,
    'proFleet': PRO_FLEET,
    'proPricing': PRO_PRICING,
    'careRecos': CARE_RECOS,
    'redirectPages': REDIRECT_PAGES,
}

# planDetail is shared (no full locale set in PLAN_DETAIL outside fr/en above)
# Add the fr+en keys under `planDetail` namespace
PLAN_DETAIL_NS = 'planDetail'


def main() -> int:
    for loc in ['fr', 'en', 'es', 'de', 'it', 'pt', 'nl']:
        path = LOCALES_DIR / f'{loc}.json'
        with path.open(encoding='utf-8') as f:
            data = json.load(f)

        # Add the shared planDetail namespace (fr/en source, others fallback to en)
        plan_src = PLAN_DETAIL.get(loc) or PLAN_DETAIL['en']
        data[PLAN_DETAIL_NS] = plan_src

        # Add each namespace
        for ns, source_dict in ALL_NAMESPACES.items():
            # Use the locale's own translation if available, else EN fallback
            data[ns] = source_dict.get(loc) or source_dict['en']

        with path.open('w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
            f.write('\n')

        print(f'{loc}: wrote {len(ALL_NAMESPACES) + 1} namespaces')

    return 0


if __name__ == '__main__':
    raise SystemExit(main())
