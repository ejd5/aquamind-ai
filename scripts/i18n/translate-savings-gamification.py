def apply_namespace(data: dict, namespace: str, keys: dict) -> None:
    """Overwrite a top-level namespace (we own these namespaces)."""
    data[namespace] = keys


def main() -> int:
    locales = ['fr', 'en', 'es', 'de', 'it', 'pt', 'nl']
    for loc in locales:
        path = LOCALES_DIR / f'{loc}.json'
        with path.open('r', encoding='utf-8') as f:
            data = json.load(f)

        apply_namespace(data, 'savings', SAVINGS[loc])
        apply_namespace(data, 'gamification', GAMIFICATION[loc])

        with path.open('w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
            f.write('\n')

        sv = len(data.get('savings', {}))
        gm = len(data.get('gamification', {}))
        print(f'  {loc}: savings={sv} gamification={gm}')

    # Consistency check
    print('\nConsistency check:')
    ref_sv = len(SAVINGS['fr'])
    ref_gm = len(GAMIFICATION['fr'])
    ok = True
    for loc in locales:
        path = LOCALES_DIR / f'{loc}.json'
        with path.open('r', encoding='utf-8') as f:
            d = json.load(f)
        sv = len(d.get('savings', {}))
        gm = len(d.get('gamification', {}))
        status = '✓' if (sv == ref_sv and gm == ref_gm) else '✗'
        if status == '✗':
            ok = False
        print(f'  {loc}: savings={sv}/{ref_sv} gamification={gm}/{ref_gm} {status}')
    print(f"\n{'OK' if ok else 'FAIL'} — {ref_sv + ref_gm} keys × {len(locales)} locales")
    return 0 if ok else 1


if __name__ == '__main__':
    raise SystemExit(main())
