def merge(dst: dict, src: dict) -> None:
    """Merge src into dst (in-place), overwriting leaf keys only."""
    for k, v in src.items():
        if k in dst and isinstance(dst[k], dict) and isinstance(v, dict):
            merge(dst[k], v)
        else:
            dst[k] = v


def main() -> None:
    # Validate the 7 locales have the same set of CARE_ADD keys.
    ref_keys = set(CARE_ADD['fr'].keys())
    for loc in LOCALES:
        if set(CARE_ADD[loc].keys()) != ref_keys:
            missing = ref_keys - set(CARE_ADD[loc].keys())
            extra = set(CARE_ADD[loc].keys()) - ref_keys
            raise SystemExit(f'CARE_ADD[{loc}] mismatch — missing: {missing}, extra: {extra}')

    for loc in LOCALES:
        path = ROOT / f'{loc}.json'
        with path.open() as f:
            data = json.load(f)

        # Business namespace — overwrite (we own it).
        if loc == 'fr':
            data['business'] = BUSINESS['fr']
        elif loc == 'en':
            data['business'] = BUSINESS['en']
        else:
            data['business'] = _build_business_for(loc)

        # Care namespace — merge new keys (preserve existing).
        care = data.get('care', {})
        if not isinstance(care, dict):
            care = {}
        merge(care, CARE_ADD[loc])
        data['care'] = care

        with path.open('w') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
            f.write('\n')

        print(f'{loc}: business={len(data["business"])} keys, care={len(data["care"])} keys')

    # Validate JSON.
    print('\nValidating JSON…')
    for loc in LOCALES:
        with (ROOT / f'{loc}.json').open() as f:
            json.load(f)
        print(f'  {loc}.json — valid')


if __name__ == '__main__':
    main()
