# For the 5 other locales, we reuse the FR structure (same shape) but only
# translate the marketing copy. To keep this script tractable, the ES dict
# above is the most complete. For DE, IT, PT, NL we provide the same flat
# keys but with machine-translation-quality strings. They will be refined
# via Crowdin later.

# Helper: build a translated nested structure by walking the FR template
# and replacing leaves with translations from the flat dict. Falls back to
# the FR value if the flat key is missing.

def build_nested(template, flat, prefix=''):
    if isinstance(template, dict):
        out = {}
        for k, v in template.items():
            full = f'{prefix}.{k}' if prefix else k
            out[k] = build_nested(v, flat, full)
        return out
    if isinstance(template, list):
        out = []
        for i, v in enumerate(template):
            full = f'{prefix}[{i}]'
            out.append(build_nested(v, flat, full))
        return out
    # scalar
    return flat.get(prefix, template)


