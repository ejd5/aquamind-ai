def merge(dst: dict, src: dict, overwrite: bool = False) -> None:
    """Recursively merge src into dst. If overwrite, replaces whole subtrees
    when both values are dicts (used for the stripScan namespace which we own).
    Otherwise, deep-merges keys that don't exist yet."""
    for k, v in src.items():
        if k not in dst:
            dst[k] = v
        elif isinstance(dst[k], dict) and isinstance(v, dict):
            if overwrite:
                dst[k] = v
            else:
                merge(dst[k], v, overwrite=False)
        elif overwrite:
            dst[k] = v
        # else: keep existing (do not overwrite)


def main() -> None:
    for locale, patches in LOCALES.items():
        path = ROOT / f'{locale}.json'
        with path.open('r', encoding='utf-8') as f:
            data = json.load(f)

        # `stripScan` is a top-level namespace we own — overwrite entirely.
        if 'stripScan' in patches:
            data['stripScan'] = patches['stripScan']

        # `gates` namespace — merge (don't overwrite other gates that might exist).
        if 'gates' in patches:
            data.setdefault('gates', {})
            merge(data['gates'], patches['gates'], overwrite=False)

        # `modules.waterTest` additions — merge without overwriting.
        if 'modules.waterTest' in patches:
            data.setdefault('modules', {}).setdefault('waterTest', {})
            merge(data['modules']['waterTest'], patches['modules.waterTest'], overwrite=False)

        # `diagnostic` additions — merge without overwriting.
        if 'diagnostic' in patches:
            data.setdefault('diagnostic', {})
            merge(data['diagnostic'], patches['diagnostic'], overwrite=False)

        with path.open('w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
            f.write('\n')
        print(f'  ✓ {locale}.json updated')

    # Sanity-check: every locale has the same stripScan key count.
    counts = {}
    for locale in LOCALES:
        with (ROOT / f'{locale}.json').open('r', encoding='utf-8') as f:
            d = json.load(f)
        counts[locale] = len(d.get('stripScan', {}))
    print('\nStripScan key counts per locale:')
    for loc, n in counts.items():
        print(f'  {loc}: {n} top-level keys')
    if len(set(counts.values())) != 1:
        print('  ⚠️  Counts differ — investigate!')


if __name__ == '__main__':
    main()
