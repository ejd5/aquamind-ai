# Sanity check: all locales must have exactly the same set of keys as FR.
FR_KEYS = set(FR.keys())
for loc, d in DICTS.items():
    keys = set(d.keys())
    missing = FR_KEYS - keys
    extra = keys - FR_KEYS
    if missing or extra:
        print(f"[{loc}] KEY MISMATCH — missing={sorted(missing)} extra={sorted(extra)}")
        sys.exit(1)
print(f"All 7 locales have {len(FR_KEYS)} keys (matched).")

# Write each locale's `partners` namespace (idempotent overwrite).
for loc in LOCALES:
    path = LOCALES_DIR / f"{loc}.json"
    if not path.exists():
        print(f"[{loc}] SKIP — file missing: {path}")
        continue
    with open(path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    data['partners'] = DICTS[loc]
    with open(path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        f.write('\n')
    # Verify
    with open(path, 'r', encoding='utf-8') as f:
        json.load(f)
    print(f"[{loc}] OK — partners namespace written ({len(DICTS[loc])} keys)")

print("\nDone. All 7 locales updated.")
